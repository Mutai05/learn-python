# arithmetic operators

print(10 + 3)

x = 10
x = x + 5

# augmented operators

print(x)

y = 5
y += 5

print(y)

# comparison operators

b = 20 > 10

print(b)

c = 5 == 4

print(c)

# logical operators

price = 15
print(price > 10 and price < 30)
print(price > 20 or price < 30)
print(not price > 10)